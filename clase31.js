/*
SWAPI.co ha desaparecido
Desafortunadamente, swapi.co ya no se mantiene y el servicio está actualmente
inactivo. El autor del proyecto, Paul Hallett, quien creó y le dio mantenimiento
ha desactivado desde hace tiempo esta API que muchos utilizamos en nuestros
proyectos de JavaScript para aprender a integrar un backend a un frontend.

SWAPI.dev, una nueva solución
Juriy Bura, junto a otros desarrolladores, han publicado una versión idéntica
a la API utilizada por Swapi.io, la cual está disponible desde el dominio
swapi.dev. Por lo tanto, de ahora en adelante, para continuar el curso sin
problemas solo debes reemplazar swapi.io, la URL obliterada
(la que ya no funciona), por la nueva versión que sí está disponible: swapi.dev.

Este proyecto es mantenido por la comunidad y gracias a la filosofía del código
libre es posible tener una nueva versión de la API para nuestros proyectos.

Crea tu propia API de Star Wars
¿Quieres tener tu propia versión de SWAPI?

El código fuente del proyecto está disponible en GitHub, lo que te permite crear
tu propia versión con solo realizar un Fork y subirlo a un servidor o consumirla
en tu localhost.
Este es el repositorio del proyecto original: https://github.com/phalt/swapi.

¡Compártenos en los comentarios en link a tu repositorio fork de la API de S
tar Wars y qué cambios hiciste o planeas hacer en tu versión de este proyecto!

*/
